<h4><a name="1">Práca s požiadavkami na predmet počas rozvrhovej akcie:</a></h4>
<br />
Všetky predmety, ktorých garantom je prihlásený používateľ, sú rozdelené na predmety bez zadaných požiadaviek a predmety so zadanými
požiadavkami.
<br />
<br />
Formulár, ktorý obsahuje zoznam všetkých predmetov, ktorým je daný používateľ garantom, je zobrazený nasledovne:
<br />
<br />
<img
	src="help/sources/garant/garantPoziadavkaZoznam.JPG" width="706"
	height="270" alt="zoznam požiadaviek garanta" align="middle" />
<br />
<b><i>Formulár obsahuje:</i></b>
<ol>
	<li>Zoznam všetkých predmetov, ktoré ešte nemajú zadané požiadavky(obrázok A.)</li>
	<li>Zoznam všetkých predmetov, ktoré už majú zadané požiadavky (obrázok	B.)</li>
	<li>Pomocou kliknutia na názov predmetu v zozname predmetov bez zadaných požiadaviek, je možné zobraziť formulár na zadanie požiadavky (obrázok A.)</li>
	<li>Pomocou tlačidla upraviť (obrázok C.) je možné počas rozvrhovej akcie upraviť zadané požiadavky</li>
</ol>
<br />
<br />
