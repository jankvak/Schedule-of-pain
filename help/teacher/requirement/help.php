<a name="3"><b><u>Editovanie predmetov so zadanými požiadavkami</u></b></a>
<br />
<br />
Postupuje sa analogicky ako pri zadávaní nových požiadaviek na
prednášku.
<br />
<br />
<B>Poznámka: </B>
Pri editovaní existujúcich požiadaviek sa nevymazávajú predchádzajúce
požiadavky. Pri tvorbe rozvrhu sa berie tá požiadavka, ktorá bola zadaná
ako posledná. (Rozhoduje sa podľa dátumu zadania požiadavky)
<br />
<br />
Systém umožňuje podporu diskusie medzi zadávateľom a rozvrhárom a to pri predmetoch
ktoré už majú zadefinované požiadavky, pomocou formulára, ktorého časť je zobrazená
na nasledujúcom obrázku:
<br />
<br />
<img src="help/sources/teacher/teacherComment1.JPG" width="605" height="230" alt="komentár"/>
<br />
<a name="4"><b><u>Postup pridania komentáru na predmet</u></b></a>
<br />
<br />
<ol>
    <li>Zadať do poľa <b>Komentár k diskusii</b> (Obrázok A.) poznámku, ktorá sa odošle rozvrhárovi</li>
    <li>Pomocou tlačidla <b>Odoslať komentár</b> (Obrázok B.) odoslať poznámku</li>
</ol>
<br />
Vyjadrenie rozvrhára k poznámke sa vykoná na konci požiadavky (pod tlačidlom: Odoslať komentár).
<br />
<br />
