<h4><a name="1">Práca s požiadavkami na predmet počas rozvrhovej akcie:</a></h4>
<br />
Všetky predmety, ktoré prednášajúci prednáša, sú rozdelené na predmety bez zadaných požiadaviek a predmety so zadanými
požiadavkami.
<br />
<br />
Formulár, ktorý obsahuje zoznam všetkých predmetov, ktoré danný používateľ
prednáša je zobrazený nasledovne:
<br />
<br />
<img
	src="help/sources/teacher/teacherPoziadavka.JPG" width="731"
	height="524" align="middle" alt="teacherPoziadavka" />
<br />
<b><i>Formulár obsahuje:</i></b>
<ol>
	<li>Zoznam všetkých predmetov, ktoré ešte nemajú zadané požiadavky
	(obrázok A.)</li>
	<li>Zoznam všetkých predmetov, ktoré už majú zadané požiadavky (obrázok
	B.)</li>
	<li>Zadávateľ požiadavky a čas zadania poslednej požiadavky (obrázok C.)</li>
        <li>Pomocou kliknutia na názov predmetu v zozname predmetov bez zadaných požiadaviek,
        je možné zobraziť formulár na zadanie požiadavky (obrázok A.)</li>
        <li>Pomocou tlačidla upraviť (obrázok D.) je možné počas rozvrhovej akcie
            upraviť zadané požiadavky</li>
</ol>
<br />
<br />
<a name="2"><b><u>Postup zadávania nových požiadaviek na predmet</u></b></a>
<br />
<br />
<ol>
	<li>Kliknúť na predmet v zozname predmetov nezadaných požiadaviek
	(viď. vyššie)</li>
	<li>Zobrazí sa nový formulár znázornený na nasledujúcom obrázku <br />
	<br />
	<img src="help/sources/teacher/teacherPoziadavkaNew.JPG" width="676"
		height="777" align="middle" alt="teacherPoziadavkaNew" /> <br />

	<b>Poznámka:</b> Vo formuláry sa zobrazí počet študentov spolu s ich rozdelením
        podľa štúdijných odborov a ročníkov (Potrebné kliknúť na text: <i>Počet zapísaných študentov</i>), ktorí majú
	zapísaný daný predmet (obrázok A.). Na základe tohto počtu študentov je
	možné nadefinovať požiadavky na rozvrh. <br />
	<br />
        </li>
        <li>Pomocou možnosti: <b>Vyplniť podľa minuloročného predmetu</b> (obrázok B.) je možné podľa
        zvoleného predmetu vyplniť požiadavky na predmet, ktorého požiadavky chceme zadať.</li>
        </ol>
        <br />
        Niekedy sa môže stať, že rozvrhár zablokuje preberanie požiadavky podľa minuloročného predmetu,
        napr. z dôvodu zmeny rozsahu hodín predmetu. V takom prípade je potrebné celú požiadavku
        vytvoriť nanovo. Blokovanie preberania požiadavky je zobrazené na nasledujúcom obrázku:
        <br />
        <br />
        <img src="help/sources/teacher/teacherPoziadavkaBlokovana.JPG" width="642" height="276" alt="poziadavka"/>
        <br />
        <ol start="4">
        <li>V okne <b>Poznámka k požiadavke</b> (obrázok C.) je možné definovať bližšie požiadavky na prednášky</li>
        <li>V okne <b>Poznámka na softvér</b> (obrázok D.) je možné definovať bližšie požiadavky na výučbový softvér</li>
	<li>Potrebné nastaviť počet rozložení (obrázok E.) max. 3 - je možné definovať
	až 3 typy rozloženia prednášok na semester <br />
	<br />
	<b>Ďalej sa pokračuje definovaní požiadavok na aktuálne rozloženie č. 1</b>
	<br />
	<br />
	</li>
	<li>Vybrať počet prednášok v týždni (obrázok F.) max. 3 - určí sa počet
	prednášok ktoré sa budú v jednom týždni konať <br />
	<br />
	<b>Ďalej sa pokračuje vyplňaním požiadavok na jednotlivé prednášky v týždni.
	Počet prednášok, na ktoré je treba vyplniť požiadavky závisí od
	vybrania počtu prednášok v týždni (obrázok F.)</b> <br />
	<br />
	</li>
	<li>Vybrať možnosť pre konanie prednášok pre celý týždeň (obrázok G.)
	alebo ručne zakliknúť jednotlivé týždne kedy bude daná prednáška
	(obrázok H.)</li>
	<li>Zadať počet hodín prednášky (obrázok I.)</li>
	<li>Zadať počet študentov na danú prednášku (obrázok J.)</li>
	<li>Vybrať potrebné vybavenie na prednášku (obrázok K.)</li>
        <li>Zadať počet stoličiek, ktoré je potrebné zabezpečiť aby bola
	zabezpečená kapacita miestnosti pre celkový počet študentov na danú
	prednášku (obrázok L.)</li>
        <li>Vybrať požadovanú kapacitu miestnosti (obrázok M.)</li>
	<li><b>Vybrať konkrétnu miestnosť</b> (obrázok N.) kliknutím na tento text
            a vybrať z ponúkaného zoznamu (obrázok O.)požadovanú mienstnosť. Miestnosti 
	v okne(obrázok P.) je možné tiež vybrať ako preferované. Tieto miestnosti sa zobrazujú
        podľa vyššie zadanej kapacity.</li>
	<li>Vybrať špeciálne požiadavky na predmet (obrázok Q.)</li>
	<li>Ak nie je možnosť vo formuláry zadať potrebnú požiadavku je možné
	ju slovne popísať v textovom poli (obrázok R.) <br />
	<br />
	<b>V prípade viacerých prednášok v týždni sa postupuje analogicky ako
	pri zadávaní požiadaviek na jednu prednášku v týždni.</b> <br />
	<br />
	<b>Po zadefinovaní požiadaviek na aktuálne Rozloženie č. 1 sa pokračuje
	definovaním požiadaviek na ďalšie rozloženia ak boli zadané viaceré
	(obrázok E.).</b> <br />
	<br />
	</li>
	<li>Po vyplnení požiadavky na daný predmet je možné formulár uložiť
	(obrázok S.) <br />
	<br />
	Po uložení požiadaviek sa daná požiadavka zobrazí v zozname už
	definovaných požiadaviek, ktoré je možné upravovať. Zoznam požiadaviek
	je zobrazený na nasledujúcom obrázku: <br />
	<br />
	<img src="help/sources/teacher/teacherPoziadavkaZoznam.JPG" width="709"
		height="524" align="middle" alt="teacherPoziadavkaZoznam" /> <br />
	Po stlačení tlačidla <b>Upraviť</b> je možné danú požiadavku zmeniť.</li>
</ol>
