		<br />
		<br />
		<b>V prípade viacerých skupín cvičení je potrebné stlačiť šípku (obrázok Q.) a následne vyššie spomínaným spôsobom vyplniť potrebné údaje.</b>
		<br />
		<br/>
	</li>
	<li>Ak nie je možnosť vo formulári zadať potrebnú požiadavku, je možné ju slovne popísať v textovom poli (obrázok R.) 
		<br />
		<br />
		<b>Po zadefinovaní požiadaviek na aktuálne Rozloženie č. 1 sa pokračuje definovaním požiadaviek na ďalšie rozloženia ak boli zadané viaceré (obrázok E.) </b>
		<br />
		<br />
	</li>	
	<li>Po vyplnení požiadavky na daný predmet je možné formulár uložiť (obrázok S.) 
