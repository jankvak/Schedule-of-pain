<h4><a name="6">Práca s požiadavkami na cvičenia mimo rozvrhovej akcie:</a></h4>
<br />
Používateľ má možnosť len prezerať zadané požiadavky na predmet. To zanmená, že v zozname predmetov sa
mu nezobrazia predmety, ktoré nemajú zadané požiadavky.
<br />
<br />
Predmety so zadanými požiadavkami môže len prezerať: T.z. že nemôže ukladať zmenené požiadavky a ani nemôže preberať požiadavky z minulého roku. 
Mimo rozvrhovej akcie je mu umožnené len diskutovať s rozvrhárom v spodnej časti formulára v poli <b>Komentár k diskusii</b>.
<br />
<br />
