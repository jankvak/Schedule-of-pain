<h4><a name="1">Práca s požiadavkami na cvičenia počas rozvrhovej akcie:</a></h4>
<br />
Všetky predmety, ktoré cvičiaci cvičí, sú rozdelené na predmety bez zadaných požiadaviek a predmety so zadanými požiadavkami.
<br />
<br />
Formulár, ktorý obsahuje zoznam všetkých predmetov, ktoré daný používateľ cvičí, je zobrazený nasledovne:
<br />
<br />
<img
	src="help/sources/pract/practPoziadavkaZoznam.JPG" width="705"
	height="527" align="middle" alt="zoznam požiadaviek na cvičenia" />
<br />
<b><i>Formulár obsahuje:</i></b>
<ol>
	<li>Zoznam všetkých predmetov, ktoré ešte nemajú zadané požiadavky (obrázok A.)</li>
	<li>Zoznam všetkých predmetov, ktoré už majú zadané požiadavky (obrázok	B.)</li>
	<li>Zadávateľa požiadavky a čas zadania poslednej požiadavky (obrázok C.)</li>
    <li>Pomocou kliknutia na názov predmetu v zozname predmetov bez zadaných požiadaviek, je možné zobraziť formulár na zadanie požiadavky (obrázok A.)</li>
    <li>Pomocou tlačidla upraviť (obrázok D.) je možné počas rozvrhovej akcie upraviť zadané požiadavky</li>
</ol>
<br />
<br />
<a name="2"><b><u>Postup zadávania nových požiadaviek na cvičenia</u></b></a>
<br />
<br />
<ol>
	<li>Kliknúť na predmet v zozname predmetov nezadaných požiadaviek (viď. vyššie)</li>
	<li>Zobrazí sa nový formulár znázornený na nasledujúcom obrázku 
		<br />
		<br />
		<img src="help/sources/pract/practPoziadavkaNew.JPG" width="642"
			height="823" align="middle" alt="formulár pre zadanie novej požiadavky" /> 
		<br />
		<b>Poznámka:</b> Vo formulári sa zobrazí počet študentov spolu s ich rozdelením
        podľa štúdijných odborov a ročníkov (Potrebné kliknúť na text: <i>Počet zapísaných študentov</i>), ktorí majú
		zapísaný daný predmet (obrázok A.). Na základe tohto počtu študentov je
		možné nadefinovať požiadavky na rozvrh. 
		<br />
		<br />
    </li>
    <li>Pomocou možnosti: <b>Vyplniť podľa minuloročného predmetu</b> (obrázok B.) je možné podľa
		zvoleného predmetu vyplniť požiadavky na cvičenia predmetu, ktorého požiadavky chceme zadať.
		<br />
		<br />
		Niekedy sa môže stať, že rozvrhár zablokuje preberanie požiadavky podľa minuloročného predmetu, napr. z dôvodu zmeny rozsahu hodín predmetu. V takom
		prípade je potrebné celú požiadavku vytvoriť nanovo. Blokovanie	preberania požiadavky je zobrazené na nasledujúcom obrázku:
		<br/>
		<br />
		<img src="help/sources/pract/practPoziadavkaBlokovana.JPG" width="620"
			height="300" align="middle" alt="prípad blokovania preberania požiadavky" /> 
		<br />
	</li>
    <li>V okne <b>Poznámka k požiadavke</b> (obrázok C.) je možné definovať bližšie požiadavky na cvičenia</li>
    <li>V okne <b>Poznámka na softvér</b> (obrázok D.) je možné definovať bližšie požiadavky na výučbový softvér</li>
	<li>Potrebné nastaviť počet rozložení (obrázok E.) max. 3 - je možné definovať až 3 typy rozloženia prednášok na semester 
		<br />
		<br />
		<b>Ďalej sa pokračuje definovaním požiadaviek na aktuálne rozloženie č. 1</b>
		<br />
		<br />
	</li>
	<li>Vybrať počet cvičení v týždni (obrázok F.) max. 3 - určí sa počet rôznych cvičení, ktoré sa budú v jednom týždni konať</li>
	<li>Vybrať možnosť pre konanie cvičení pre všetky týždne semestra (obrázok G.) alebo ručne zakliknúť jednotlivé týždne, kedy sa budú konať cvičenia (obrázok H.)	
		<br />
		<br />
		<b>Ďalej sa pokračuje vypĺňaním požiadaviek na jednotlivé cvičenia v týždni. Počet cvičení, na ktoré je treba vyplniť požiadavky, závisí od
		vybraného počtu cvičení v týždni (obrázok F.)</b> 
		<br />
		<br />
	</li>
	<li>Zadať počet hodín cvičení v týždni (obrázok I.)</li>
	<li>Zadať maximálny počet cvičení, ktoré môžu prebiehať súčasne (obrázok J.)</li>
	<li>Vybrať potrebné vybavenie na cvičenia (obrázok K.)</li>
	<li>Zadať počet študentov na cvičenie v danej skupine (obrázok L.) </li>
	<li>Vybrať typ miestnosti, v ktorej sa budú konať cvičenia (obrázok M.) </li>
	<li>Vybrať kapacitu miestnosti (obrázok N.) </li>
	<li>V prípade, že má používateľ záujem o konkrétnu miestnosť, stačí iba kliknúť na text <b>Vybrať konkrétnu miestnosť</b> (obrázok O.) 
