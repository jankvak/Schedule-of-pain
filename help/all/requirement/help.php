<hr />
<br />
<br />
Používateľ systému má možnosť si zvoliť z 3 typov časovej priority v
týždňi.
<br />
<br />
<b>Preferovane</b>
- v daný čas používateľovi najviac vyhovuje výučba predmetov
<br />
<b>Nevyhovujúce</b>
- v daný čas používateľovi vôbec nevyhovuje výučba predmetov
<br />
<b>OK</b>
- ak chceme nastaviť danný čas ako defaultne nevyplnený
<br />
<br />
Používateľ počas rozvrhových akcií môže zadávať osobné časové priority, vkladať
komentár k prioritám a preberať časové priority z minulého roku. V prípade, keď nie
je rozvrhová akcia, používateľ môže len prezerať zadané časové priority.
<br />
