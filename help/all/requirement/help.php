<a name=2></a>
<b><u>Postup prebratia časových priorít z minulého roku</u></b>
<br />
<br />
<ol>
    <li>Vybrať možnosť <b>Prebrať osobné časové priority z minulého roku</b> (obrázok D.)</li>
    <li>Odoslať danú požiadavku (obrázok E.)</li>
</ol>
<br />
<br />
<a name=3></a>
<b><u>Postup zadávania osobných časových priorít</u></b>
<br />
<br />
<ol>
	<li>Vybrať možnosť <b>Výber priority </b>(obrázok A.)</li>
	<li>Kliknúť na dané políčko v rozvrhu (obrázok B.)</li>
	<li>Napísať dôvod pri nevyhovujúcich požiadavkách do poľa <b>Komentár</b>
	(obrázok C.) - nepovinný krok</li>
	<li>Odoslať danú požiadavku (obrázok E.)</li>
</ol>
<br />
<br />
<a name=4></a>
<b><u>Postup úpravy osobných časových priorít</u></b>
<br />
<br />
<ol>
	<li>Postupuje sa rovnako ako pri <i>Postupe zadávaní osobných časových priorít</i></li>
</ol>
<br />
